#include "RPN.hpp"

/* ************************************************************************** */
/* *********************************  MAIN  ********************************* */
/* ************************************************************************** */

int main(int argc, char *argv[])
{
    if (argc != 2)
    {
        std::cout << "Error" << std::endl;
        return EXIT_FAILURE;
    }
    try
    {
        input_to_array(argv[1]);
    }
    catch(const std::exception& e)
    {
        std::cout << e.what() << '\n';
    }

    return EXIT_SUCCESS;
}
